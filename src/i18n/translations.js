// Get nested key: 'nav.home' -> obj.nav.home
function getNested(obj, key) {
  return key.split('.').reduce((o, k) => (o && o[k] != null ? o[k] : undefined), obj);
}

export function getT(translationsObj, locale) {
  const localeData = translationsObj[locale] || translationsObj.en;
  return (key, replacements = {}) => {
    let str = getNested(localeData, key);
    if (str == null) str = getNested(translationsObj.en, key) || key;
    Object.entries(replacements).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    });
    return str;
  };
}
